# Рекурсивна функція -> це функція яка визначається в термінах самої себе і може викликати саму себе


# 5! -> 1 * 2 * 3 * 4 * 5 -> 5 * 4! -> 5 * 4 * 3! -> 5 * 4 * 3 * 2! -> 5 * 4 * 3 * 2 * 1
# 7!

def factorial(n):
    if n <= 1:
        return 1
    else:
        return n * factorial(n - 1)


print(factorial(5))

