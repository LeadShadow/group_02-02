# Переменная length содержит длину, а переменная width ширину — комнаты. Необходимо
# произвести расчет площади комнаты и результат поместить в переменную area. Добавьте
# переменную show, в которую поместите строку со следующим шаблоном: 'With width
# <значение ширины> and length <значение длины> of the room, its area is equal to <значение площади>'.

# S = a * b
length = 2.75
width = 1.75
area = length * width
show = f"With width {width} and length {length} of the room, its area is equal to {area}"
print(show)
